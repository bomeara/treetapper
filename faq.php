<p><br><b><a name="whatisthiswebsite">What is this website?</a></b>

<br>TreeTapper.org was originally part of the postdoctoral project of <a href="http://www.brianomeara.info" target="_blank">Brian O'Meara</a>, and its design started in Nov. 2007. Initial development of this project was funded by the National Evolutionary Synthesis Center (<a href="http://www.nescent.org" target="_blank">NESCent</a>). NESCent, in turn, is funded by NSF grant <a href="http://www.nsf.gov/awardsearch/showAward.do?AwardNumber=0423641" target="_blank">EF-0423641</a>.<br>The initial postdoc <a href="OMearaPostdocProposal.pdf">proposal</a> (PDF) describes the motivation for the site in in more depth.
</p>
<p><br><b><a name="isitready">Is it ready?</b></a>

<br>This site is still VERY preliminary and not intended for wide distribution, but if you have suggestions, please email them to Brian at bcomeara _at_ nescent.org. Once the site is more finished, you'll be able to do queries for methods and software, add new methods and tools, find which methods or tools still need to be written, link to stable URIs for methods and tools, and even download the whole database (minus sensitive user info). You can see what I'm up to on the <a href="http://treetapper-dev.blogspot.com">development blog</a>.
</p>
<p><br><b><a name="felsenstein">Didn't Joe Felsenstein already do this?</b></a>

<br>While that's generally a safe assumption in phylogenetics, this site and Felsenstein's site of <a href="http://evolution.genetics.washington.edu/phylip/software.html" target="_blank">phylogeny programs</a> differ in important ways. Felsenstein's <a href="http://evolution.genetics.washington.edu/phylip/software.html" target="_blank">site</a> lists a wide range of programs related to phylogenetics, including tree-building programs. This site will only have programs that use phylogenies to understand evolution -- tree-building, consensus trees, etc. are currently beyond the scope of the site. On the other hand, Felsenstein's <a href="http://evolution.genetics.washington.edu/phylip/software.html" target="_blank">site</a> is focused just on existing programs (which are characterized by method, platform, etc.); this site will include questions and methods, even those not implemented in current software, making it much easier to find areas which still need more work.
</p>
<p><br><b><a name="image">What's the image on the home page?</b></a>

<br>It is a picture of maple trees being tapped for sap in the spring. The sap is boiled down to create maple syrup. It's a potentially corny metaphor for this site: with the right tools (spigots and buckets / software) you can get something otherwise unavailable (syrup / information about biology) using a tree that is not destroyed in the process (sugar maple / phylogeny). The actual image was purchased from <a href="http://www.istockphoto.com">iStockPhoto</a>.
</p>

